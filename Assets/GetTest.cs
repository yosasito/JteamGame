using UnityEngine;

public class GetTest : MonoBehaviour
{
    private void OnCollisionEnter(Collision collision)
    {
        // 衝突したオブジェクトのタグが "Test2" なら削除
        if (collision.gameObject.CompareTag("Player"))
        {
            Destroy(this.gameObject);
            Debug.Log("Keyを削除しました！");
        }
    }
}